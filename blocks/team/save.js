/**
 * Save function for the Team block
 */
const TeamSave = ({ attributes }) => {
    const { 
        id, 
        layout,
        alignment,
        showPosition,
        showDescription,
        showSocialIcons,
        imageShape,
        animation,
        anchor,
        customClasses,
        members
    } = attributes;

    // Build class names
    const blockClasses = [
        "digiblocks-team-block",
        `layout-${layout}`,
        animation !== "none" ? `animate-${animation}` : "",
        customClasses || "" // Add custom classes if they exist
    ]
        .filter(Boolean)
        .join(" ");

	// Build common props
    const commonProps = {
        className: blockClasses,
        id: anchor || null,
        "data-custom-id": id
    };

    return (
        <div {...commonProps}>
            <div className="digiblocks-team-container">
				{(members || []).map((member, index) => (
                    <TeamMemberComponent
                        key={member.id}
                        member={member}
                        imageShape={imageShape}
                        showPosition={showPosition}
                        showDescription={showDescription}
                        showSocialIcons={showSocialIcons}
                        alignment={alignment}
                    />
                ))}
            </div>
        </div>
    );
};

export default TeamSave;